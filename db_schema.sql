-- Global feature flags (system-wide)
create table if not exists public.feature_flags (
  id int primary key default 1,
  show_feed boolean not null default true,
  show_activities boolean not null default true,
  show_attendance boolean not null default true,
  show_projects boolean not null default true,
  show_resources boolean not null default true,
  show_chat boolean not null default true,
  show_showcase boolean not null default true,
  show_suggestions boolean not null default true,
  show_challenges boolean not null default true,
  show_roadmap boolean not null default true,
  show_community boolean not null default true,
  show_playground boolean not null default true,
  show_games boolean not null default true,
  updated_at timestamptz not null default now()
);

insert into public.feature_flags (id)
values (1)
on conflict (id) do nothing;

alter table public.feature_flags enable row level security;

drop policy if exists "Feature flags select" on public.feature_flags;
drop policy if exists "Feature flags update" on public.feature_flags;

create policy "Feature flags select"
  on public.feature_flags for select
  using (auth.role() = 'authenticated');

create policy "Feature flags update"
  on public.feature_flags for update
  using (
    exists (
      select 1 from public.users u
      where u.uid = auth.uid()::text
        and u.role = 'PATRON'
    )
  );

create table if not exists public.teams (
  id bigint generated by default as identity primary key,
  name text not null,
  description text,
  created_by text not null references public.users(uid) on delete cascade,
  created_at timestamptz not null default now()
);

create table if not exists public.team_members (
  id bigint generated by default as identity primary key,
  team_id bigint not null references public.teams(id) on delete cascade,
  user_uid text not null references public.users(uid) on delete cascade,
  joined_at timestamptz not null default now(),
  unique (team_id, user_uid)
);

create table if not exists public.team_join_requests (
  id bigint generated by default as identity primary key,
  team_id bigint not null references public.teams(id) on delete cascade,
  requester_uid text not null references public.users(uid) on delete cascade,
  status text not null default 'PENDING',
  created_at timestamptz not null default now(),
  reviewed_by text references public.users(uid) on delete set null,
  reviewed_at timestamptz
);

create unique index if not exists idx_team_join_requests_unique
  on public.team_join_requests(team_id, requester_uid);

create table if not exists public.team_challenges (
  id bigint generated by default as identity primary key,
  team_id bigint not null references public.teams(id) on delete cascade,
  title text not null,
  description text,
  due_date date,
  created_by text not null references public.users(uid) on delete cascade,
  created_at timestamptz not null default now()
);

create table if not exists public.team_challenge_submissions (
  id bigint generated by default as identity primary key,
  challenge_id bigint not null references public.team_challenges(id) on delete cascade,
  user_uid text not null references public.users(uid) on delete cascade,
  note text not null,
  submitted_at timestamptz not null default now(),
  unique (challenge_id, user_uid)
);

create index if not exists idx_team_members_team_id on public.team_members(team_id);
create index if not exists idx_team_members_user_uid on public.team_members(user_uid);
create index if not exists idx_team_join_requests_team_id on public.team_join_requests(team_id);
create index if not exists idx_team_join_requests_requester_uid on public.team_join_requests(requester_uid);
create index if not exists idx_team_challenges_team_id on public.team_challenges(team_id);
create index if not exists idx_team_challenge_submissions_challenge_id on public.team_challenge_submissions(challenge_id);
create index if not exists idx_team_challenge_submissions_user_uid on public.team_challenge_submissions(user_uid);

alter table public.teams enable row level security;
alter table public.team_members enable row level security;
alter table public.team_join_requests enable row level security;
