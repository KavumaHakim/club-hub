-- Storage bucket for playground project files
insert into storage.buckets (id, name, public)
values ('project_files', 'project_files', false)
on conflict (id) do nothing;

create policy "Project files read"
  on storage.objects for select
  using (bucket_id = 'project_files' and auth.role() = 'authenticated');

create policy "Project files write"
  on storage.objects for insert
  with check (bucket_id = 'project_files' and auth.role() = 'authenticated');

create policy "Project files update"
  on storage.objects for update
  using (bucket_id = 'project_files' and auth.role() = 'authenticated');

create policy "Project files delete"
  on storage.objects for delete
  using (bucket_id = 'project_files' and auth.role() = 'authenticated');
