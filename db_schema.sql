-- Playground projects
create table if not exists public.playground_projects (
  id bigint generated by default as identity primary key,
  name text not null,
  language text not null,
  created_by text not null references public.users(uid) on delete cascade,
  team_id bigint references public.teams(id) on delete set null,
  created_at timestamptz not null default now()
);

create table if not exists public.playground_project_members (
  id bigint generated by default as identity primary key,
  project_id bigint not null references public.playground_projects(id) on delete cascade,
  user_uid text not null references public.users(uid) on delete cascade,
  added_by text not null references public.users(uid) on delete cascade,
  added_at timestamptz not null default now(),
  unique (project_id, user_uid)
);

create table if not exists public.playground_project_files (
  id bigint generated by default as identity primary key,
  project_id bigint not null references public.playground_projects(id) on delete cascade,
  path text not null,
  created_by text not null references public.users(uid) on delete cascade,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now(),
  unique (project_id, path)
);

create table if not exists public.playground_project_activity (
  id bigint generated by default as identity primary key,
  project_id bigint not null references public.playground_projects(id) on delete cascade,
  user_uid text not null references public.users(uid) on delete cascade,
  action text not null,
  detail text,
  created_at timestamptz not null default now()
);

create index if not exists idx_playground_projects_team_id on public.playground_projects(team_id);
create index if not exists idx_playground_project_members_project_id on public.playground_project_members(project_id);
create index if not exists idx_playground_project_members_user_uid on public.playground_project_members(user_uid);
create index if not exists idx_playground_project_files_project_id on public.playground_project_files(project_id);
create index if not exists idx_playground_project_activity_project_id on public.playground_project_activity(project_id);

alter table public.playground_projects enable row level security;
alter table public.playground_project_members enable row level security;
alter table public.playground_project_files enable row level security;
alter table public.playground_project_activity enable row level security;

create policy "Playground projects visible to authenticated users"
  on public.playground_projects for select
  using (
    auth.role() = 'authenticated' and (
      auth.uid()::text = created_by
      or exists (
        select 1 from public.playground_project_members ppm
        where ppm.project_id = playground_projects.id
          and ppm.user_uid = auth.uid()::text
      )
      or (team_id is not null and exists (
        select 1 from public.team_members tm
        where tm.team_id = playground_projects.team_id
          and tm.user_uid = auth.uid()::text
      ))
      or exists (
        select 1 from public.users u
        where u.uid = auth.uid()::text
          and u.role = 'PATRON'
      )
    )
  );

