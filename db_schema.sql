-- Teams policies
create policy "Teams can be viewed by authenticated users"
  on public.teams for select
  using (auth.role() = 'authenticated');

create policy "Team creator can create team"
  on public.teams for insert
  with check (auth.uid()::text = created_by);

create policy "Team creator can update team"
  on public.teams for update
  using (auth.uid()::text = created_by);

create policy "Team creator can delete team"
  on public.teams for delete
  using (auth.uid()::text = created_by);

-- Team members policies (owner-controlled)
drop policy if exists "Team members can be viewed by authenticated users" on public.team_members;
drop policy if exists "Users can join themselves" on public.team_members;
drop policy if exists "Team members can add others" on public.team_members;
drop policy if exists "Users can leave themselves" on public.team_members;

create policy "Team members can be viewed by authenticated users"
  on public.team_members for select
  using (auth.role() = 'authenticated');

create policy "Team owner can add members"
  on public.team_members for insert
  with check (
    exists (
      select 1
      from public.teams t
      where t.id = team_members.team_id
        and t.created_by = auth.uid()::text
    )
  );

create policy "Members can leave or owner can remove"
  on public.team_members for delete
  using (
    auth.uid()::text = user_uid
    or exists (
      select 1
      from public.teams t
      where t.id = team_members.team_id
        and t.created_by = auth.uid()::text
    )
  );

